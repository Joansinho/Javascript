valor = true;
valor2 = false;


//and (&&)
and = valor && valor2; //True si las dos condiciones se cumplen, False si no.


//OR (||)
or = valor || valor2; //True si una es verdadera, False si las dos son falsas


//not (!)
not = !valor2; //negar una afirmacion, si una afirmacion es true la convertira en false y viceversa
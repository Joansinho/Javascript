numero1 = 20;
numero2 = 40;


//igualdad (==)
igualdad = numero1 == numero2; //compara dos numeros. Si son iguales True, de lo contrario False


//distinto de (!=)
distinto_de = numero1 != numero2; //si son distintos True, de resto False


//identidad (===)
identidad = numero1 === numero2; //compara los tipos de datos junto con el valor.


//no identico (!==)
no_identico = numero1 !== numero2; //Si son distintos true, False.



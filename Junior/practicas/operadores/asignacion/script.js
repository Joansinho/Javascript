//asignaciones 


//adicion (+)
let adicion = 10;
adicion += 5; //le adiciona una cantidad a la variable "adicion", en este caso 5


//sustraccion (-)
let sustraccion = 10;
sustraccion -= 5; //le sustrae una cantidad a la variable


//multiplicacion (*)
let multiplicacion = 10;
multiplicacion *= 5; //multiplica una cantidad a la varible


//division (/) 
let division = 10;
division /= 2; //divide una cantidad a la variable


//resto (%)
let resto = 10;
resto %= 4; //regresa el resto de una division (cuanto le falta a la division para llegar a, cuando son exactas regresan 0)


//exponenciacion (**)
let exponenciacion = 10;
exponenciacion **=2; //eleva el numero 
